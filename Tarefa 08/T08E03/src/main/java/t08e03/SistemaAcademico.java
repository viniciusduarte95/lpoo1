    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        boolean condicao = true;
        while(condicao) {
            try {
                int quantidadeTotalProfessores, quantidadeTotalAlunos, i;

                JFrame jFrame = new JFrame();
                String getQuantidadeTotalProfessores = JOptionPane.showInputDialog(jFrame, 
                                                        "Informe a quantidade de professores que serão cadastrados:");
                quantidadeTotalProfessores = Integer.parseInt(getQuantidadeTotalProfessores);

                String getQuantidadeTotalAlunos = JOptionPane.showInputDialog(jFrame, 
                                                    "Agora, informe a quantidade de alunos que serão cadastrados:");
                quantidadeTotalAlunos = Integer.parseInt(getQuantidadeTotalAlunos);

                professores = new Professor[quantidadeTotalProfessores];
                alunos = new Aluno[quantidadeTotalAlunos];
                
                condicao = false;
            } catch(NumberFormatException nfe) {
                JOptionPane.showMessageDialog(null,"A quantidade deve ser numérica!", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_formWindowOpened

