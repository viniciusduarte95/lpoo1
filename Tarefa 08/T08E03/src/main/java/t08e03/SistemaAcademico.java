    private void beaExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_beaExcluirActionPerformed
        // TODO add your handling code here:
        JFrame jFrame = new JFrame();
        
        String nome = teaNome.getText();
        
        int i = 0;
        while(i < quantidadeAluno && !nome.equals(alunos[i].getNome())) {
            i++;
        }
        
        if (i < quantidadeAluno) {
            for (int k = i; k < quantidadeAluno; k++) {
                alunos[k] = alunos[k+1];
            }
            
            quantidadeAluno--;
            
            JOptionPane.showMessageDialog(jFrame, "Aluno excluído com sucesso!");
        } else {
            JOptionPane.showMessageDialog(jFrame, "Aluno não encontrado.");
        }
    }//GEN-LAST:event_beaExcluirActionPerformed

    private void bmpMatricularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bmpMatricularActionPerformed
        // TODO add your handling code here:
        JFrame jFrame = new JFrame();
        
        String nome = tmpNome.getText();
        String nomeDisciplina = tmpDisciplina.getText();
        
        Disciplina disciplina = new Disciplina(nomeDisciplina);
        
        int i = 0;
        while(i < quantidadeProfessor && !nome.equals(professores[i].getNome())) {
            i++;
        }

        if (i < quantidadeProfessor) {
            if (professores[i].getquantidadeDisciplinasMatriculadas() == 0) {
                boolean condicao = true;
                while(condicao) {
                    try {
                        String getQuantidadeDisciplinas = JOptionPane.showInputDialog(jFrame, 
                                                                "Digite quantas disciplinas o professor deve ser relacionado:");
                        int quantidadeDisciplinas = Integer.parseInt(getQuantidadeDisciplinas);

                        professores[i].setQuantidadeDisciplinasMatriculadas(quantidadeDisciplinas);
                        condicao = false;
                    } catch(NumberFormatException nfe) {
                        JOptionPane.showMessageDialog(null,"A quantidade deve ser numérica!", "Erro", JOptionPane.ERROR_MESSAGE);
                    }
                }
            }

            String informe = professores[i].fazMatricula(disciplina);
            JOptionPane.showMessageDialog(jFrame, informe);
        } else {
            JOptionPane.showMessageDialog(jFrame, "Professor não encontrado.");
        }
    }//GEN-LAST:event_bmpMatricularActionPerformed

